#!/bin/bash

clear

logo=$(cat <<'EOF'
      ___                      _   _             
     |_ _|_ __   ___ ___ _ __ | |_(_) ___  _ __  
      | || '_ \ / __/ _ \ '_ \| __| |/ _ \| '_ \ 
      | || | | | (_|  __/ |_) | |_| | (_) | | | |
     |___|_| |_|\___\___| .__/ \__|_|\___/|_| |_|
                        |_|                      
         ____   ___   ____ _  _______ ____         
 __/\__ |  _ \ / _ \ / ___| |/ / ____|  _ \  __/\__
 \    / | | | | | | | |   | ' /|  _| | |_) | \    /
 /_  _\ | |_| | |_| | |___| . \| |___|  _ <  /_  _\
   \/   |____/ \___/ \____|_|\_\_____|_| \_\   \/  


EOF
)

colors=(31 33 32 36 95)

i=0
while IFS= read -r line; do
    color=${colors[$((i % ${#colors[@]}))]}
    printf "\e[${color}m%s\e[0m\n" "$line"
    i=$((i+1))
    sleep 0.08
done <<< "$logo"

printf "\n"